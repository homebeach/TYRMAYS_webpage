	<h2>1 § Nimi ja kotipaikka</h2>

	<p>
	Yhdistyksen nimi on TYRMÄYS (Tampereen Yliopiston Raskaan Musiikin Äänekkäiden Ystävien Seura). Näissä säännöissä siitä käytetään nimitystä seura. Seuran kotipaikka on Tampere. Seuran virallinen kieli on suomi.</p>


	<h2>2 § Seuran tarkoitus ja toiminta</h2>
	<p>Seuran tarkoituksena on yhdistää samanhenkisiä ihmisiä Tampereen yliopistossa. Seura on suunnattu raskaan musiikin ystäville. Seurassa raskaan musiikin katsotaan tarkoittavan yleisesti rock-, metallimusiikkia sekä näiden lähes kaikkia alalajeja. Tarkoituksensa toteuttamiseksi seura voi järjestää kokouksia, ekskursioita konsertteihin ja musiikkitapahtumiin sekä tiedotus-, julkaisu- ja virkistystoimintaa. Seura voi vaikuttaa yhteisöihin ja yrityksiin taloudellisen tuen saamiseksi toiminnalleen. Lisäksi seura voi
	</p>
	<ul>
	<li>omistaa toimintaansa varten tarpeellista kiinteää tai irtainta omaisuutta;</li>
	<li>asianmukaisella luvalla harjoittaa ravitsemusliiketoimintaa sekä järjestää myyjäisiä, arpajaisia, rahakeräyksiä ja maksullisia huvitilaisuuksia;</li>
	<li>ottaa vastaan avustuksia, lahjoituksia ja testamentteja toimintansa tukemiseksi;</li>
	<li>Seuran tarkoituksena ei ole voiton tai muun välittömän taloudellisen edun hankkiminen toimintaan osallisille.</li>
	</ul>

	<h2>3 § Jäsenet</h2>
	<p>
	Seuran jäseniä ovat varsinaiset, kannatus- ja kunniajäsenet. Seuran varsinaiseksi jäseneksi voidaan hyväksyä kuka tahansa seuran toiminnasta kiinnostunut henkilö, joka on Tampereen yliopiston nykyinen tai entinen opiskelija tai kuuluu tai on kuulunut sen henkilökuntaan. Seuran kannatusjäseneksi voidaan hyväksyä seuran toiminnasta kiinnostunut henkilö, jota ei voida hyväksyä varsinaiseksi jäseneksi tai seuran toimintaa tukeva luonnollinen henkilö tai oikeustoimikelpoinen yhteisö. Kannatusjäsenille kuuluvat samat edut ja velvollisuudet kuin varsinaisille jäsenille, mutta heillä ei ole äänioikeutta seuran kokouksissa, eikä heitä voi valita seuran hallitukseen. Kunniajäsenekseen seura voi kutsua yhdistyksen toimintaa ansiokkaasti edistäneen henkilön. Ehdotuksen kunniajäseneksi kutsumisesta voi tehdä seuran varsinainen jäsen. Ehdotuksen hyväksyminen vaatii hallituksen yksimielisen päätöksen. Kunniajäsen on myös seuran varsinainen jäsen.
	</p>
	<h2>4 § Jäsenmaksut</h2>
	<p>
	Jäsenet ovat velvollisia suorittamaan toimikausittain seuralle jäsenmaksun, jonka suuruuden määrää seuran vuosikokous. Poikkeuksia ovat kuitenkin kunniajäsenet, joilta ei vaadita jäsenmaksua.
	</p>

	<h2>5 § Seurasta eroaminen ja erottaminen</h2>
	<p>
	Jäsen voi erota seurasta ilmoittamalla siitä kirjallisesti seuran hallitukselle tai sen puheenjohtajalle tai suullisesti seuran kokoukselle. Erotessaan jäsenen tulee maksaa mahdolliset näiden sääntöjen mukaiset maksunsa, joita hänellä on seuralle sekä luovuttaa seuralle kuuluva omaisuus. Jäsen katsotaan eronneeksi, mikäli hän ei ole suorittanut jäsenmaksuaan ennalta ilmoitettuun eräpäivään mennessä. Hallitus voi erottaa jäsenen, joka on toiminut törkeästi seuran periaatteita vastaan tai tuottanut seuralle huomattavaa vahinkoa. Erotetulla jäsenellä on oikeus kirjallisesti valittaa päätöksestä seuran kokoukselle, jonka täytyy tällöin vahvistaa päätös kolmen neljäsosan (3/4) enemmistöllä annetuista äänistä. Valitus on toimitettava hallitukselle kolmenkymmenen (30) vuorokauden kuluessa hallituksen kokouksen tarkastetun pöytäkirjan julkistamisesta ja se on käsiteltävä lähinnä seuraavassa seuran kokouksessa, kuitenkin viimeistään kuudenkymmenen (60) vuorokauden sisällä valituksen jättöpäivästä. 
	</p>
	<h2>6 § Seuran kokoukset</h2>
	<p>
	Seuran kokouksia ovat vuosikokous, tilikokous ja ylimääräiset kokoukset. Seura kokoontuu vuosikokoukseen 15.8.-30.9. välisenä aikana ja tilikokoukseen 1.10.-31.1. välisenä aikana. Seuran kokoukset kutsuu koolle seuran hallitus. Vuosikokouksessa on käsiteltävä ainakin
	</p>
	<ul>
	<li>hallituksen valinta seuraavaksi toimikaudeksi;</li>
	<li>seuran tilintarkastajan ja varatilintarkastajan valitseminen tulevalle toimikaudelle;</li>
	<li>seuran jäsenmaksun suuruus ja eräpäivä tulevalle toimikaudelle.</li>

	</ul>
	<p>
	Tilikokouksessa on käsiteltävä ainakin
	</p>
	<ul>
	<li>toimintakertomus edelliseltä toimikaudelta;</li>
	<li>tuloslaskelma ja tase liitetietoineen;</li>
	<li>tilinpäätöksen hyväksyminen;</li>
	<li>vastuuvapauden myöntäminen tilivelvollisille;</li>
	<li>hallituksen valmistelema toimintasuunnitelma kuluvalle toimikaudelle;</li>
	<li>hallituksen valmistelema talousarvio kuluvalle toimikaudelle.</li>

	</ul>

	<p>
	Ylimääräinen kokous pidetään, kun seuran kokous niin päättää, kun hallitus katsoo siihen olevan aihetta tai kun vähintään kymmenesosa (1/10) seuran varsinaisista jäsenistä tai viisi (5) seuran varsinaista jäsentä vaatii sitä ilmoittamansa asian käsittelyä varten kirjallisesti hallitukselta. Kokous on pidettävä neljäntoista (14) vuorokauden kuluessa vaatimuksen esittämisestä. Kutsu seuran kokoukseen on julkaistava vähintään kahdeksan (8) vuorokautta ennen kokousta seuran ilmoitustaululla tai lähettämällä kutsu sähköpostitse jäsenen ilmoittamaan osoitteeseen. Mikäli kokouksessa käsitellään seuran purkamista, hallitukselle tai sen jäsenelle esitettyä epäluottamuslausetta, sääntöjen muuttamista, hallituksen tai sen jäsenen erottamista tai hallituksen täydentämistä, on tästä mainittava kokouskutsun yhteydessä olevassa esityslistassa. 
	</p>

	<h2>7 § Oikeudet seuran kokouksissa</h2>
	<p>
	Seuran kokouksissa on läsnäolo- ja puheoikeus kaikilla seuran jäsenillä. Varsinaisilla jäsenillä on esitysoikeus sekä äänioikeus siten, että jokaisella on yksi ääni, ellei näissä säännöissä toisin määrätä. Kokouksen päätöksellä voidaan myös seuraan kuulumattomalle myöntää läsnäolo-oikeus sekä puheoikeus.
	</p>
	<h2>8 § Päätöksenteko</h2>
	<p>
	Kokous on päätösvaltainen, jos siihen osallistuu riittävä määrä seuran varsinaisia jäseniä, sekä vähintään puolet hallituksesta. Päätökset seuran kokouksessa tehdään ehdottomalla enemmistöllä annetuista äänistä, ellei näissä säännöissä toisin määrätä. Äänten jakautuessa tasan ratkaisee kokouksen puheenjohtajan mielipide, henkilövaaleissa kuitenkin arpa. Seuran kokouksista sekä hallituksen kokouksista on pidettävä päätöspöytäkirjaa, jonka allekirjoittavat kokouksen puheenjohtaja ja sihteeri. Seuran kokouksien pöytäkirjan tarkastavat kokouksen valitsemat pöytäkirjantarkastajat ja hallituksen kokouksen pöytäkirjan tarkastaa hallitus itse.</p>
	<h2>9 § Seuran hallitus</h2>

	<p>
	Seuran asioita hoitaa toimikaudeksi kerrallaan valittu hallitus. Hallitukseen kuuluu puheenjohtaja, varapuheenjohtaja, sihteeri ja rahastonhoitaja sekä kahdesta kuuteen (2-6) muuta jäsentä. Hallituksen jäsenet valitsee vuosikokous seuraavanlaisella tavalla:
	</p>

	<ol>
	<li>valitaan puheenjohtaja</li>
	<li>valitaan rahastonhoitaja</li> 
	<li>valitaan varapuheenjohtaja</li> 
	<li>valitaan sihteeri</li> 
	<li>valitaan muut hallituksen jäsenet. Vaalissa jokainen äänioikeutettu saa äänestää enintään niin montaa ehdokasta kuin on täytettäviä hallituspaikkoja.</li>
	</ol>

	<p>
	Hallituksen jäsen voi erota kesken toimikauden. Hallituksesta eronneen tilalle voidaan yhdistyksen kokouksessa valita uusi jäsen jäljellä olevaksi toimikaudeksi. 
	</p>

	<h2>10 § Hallituksen tehtävät</h2>
	<p>
	Hallituksen puheenjohtaja huolehtii hallituksen kokousten koolle kutsumisesta, johtaa puhetta hallituksen kokouksissa ja valvoo hallituksen tekemien päätösten toimeenpanoa. Hallituksen varapuheenjohtaja huolehtii puheenjohtajan tehtävistä tämän ollessa estynyt. Sihteerin tehtävänä on laatia pöytäkirjat seuran hallituksen kokouksista sekä huolehtia seuran jäsenluettelon ylläpidosta ja seuran arkistosta. Rahastonhoitajan tehtävänä on hoitaa seuran taloutta ja omaisuutta sekä valmistella ehdotukset tilinpäätökseksi ja talousarvioksi. Hallituksen tehtäviin kuuluu
	</p>
	<ul>
	<li>johtaa seuran toimintaa;</li>
	<li>valmistella seuran kokouksissa esille tulevat asiat ja toimeenpanna niissä tehdyt päätökset;</li>
	<li>hoitaa seuran taloutta ja omaisuutta;</li>
	<li>päättää jäseneksi hyväksymisen periaatteista;</li>
	<li>laatia ehdotus seuran toimintasuunnitelmaksi, talousarvioksi, toimintakertomukseksi ja tilinpäätökseksi;</li>
	<li>valvoa toimikuntien ja toimihenkilöiden toimintaa;</li>
	<li>kutsua seuran kokoukset koolle tarpeen vaatiessa;</li>
	<li>toimia seuran edustajana.</li>
	</ul>


	<h2>11 § Hallituksen kokoukset</h2>
	<p>
	Hallituksen kokous on päätösvaltainen, mikäli kokouksesta on tiedotettu hallituksen keskuudessaan sopimalla tavalla ja läsnä on vähintään puolet hallituksen jäsenistä siten, että puheenjohtaja tai varapuheenjohtaja on läsnä. Päätökset tehdään ehdottomalla enemmistöllä annetuista äänistä. Jos äänet jakautuvat tasan, ratkaisee puheenjohtajan mielipide, henkilövaaleissa kuitenkin arpa.</p>
	
	<h2>12 § Seuran nimenkirjoittajat</h2>

	<p>
	Seuran nimen kirjoittaa hallituksen puheenjohtaja tai varapuheenjohtaja yhdessä jonkun muun hallituksen jäsenen kanssa. Hallitus voi myöntää määräämäänsä tarkoitusta varten nimenkirjoitusoikeuden seuran jäsenelle enintään toimikautensa ajaksi.	
	</p>
	<h2>13 § Seuran hallinnollinen toiminta</h2>
	<p>
	Seuran taloudenhoitoa ja hallintoa valvoo tilintarkastaja. Hallituksen on toimitettava tilinpäätös tarvittavine asiakirjoineen sekä toimintakertomus tilintarkastajalle viimeistään kaksikymmentäyksi (21) vuorokautta ennen tilikokousta. Tilit on annettava tarkistettavaksi muutenkin pyydettäessä. Tilintarkastajan tulee antaa hallitukselle kirjallinen, seuran tilikokoukselle osoitettu kertomus toimittamastaan seuran hallinnon ja tilien tarkastuksesta viimeistään seitsemän (7) vuorokautta ennen tilikokousta.
	</p>
	<h2>14 § Tilintarkastus</h2>
	<p>
	Seuran taloudenhoitoa ja hallintoa valvoo tilintarkastaja. Hallituksen on toimitettava tilinpäätös tarvittavine asiakirjoineen sekä toimintakertomus tilintarkastajalle viimeistään kaksikymmentäyksi (21) vuorokautta ennen tilikokousta. Tilit on annettava tarkistettavaksi muutenkin pyydettäessä. Tilintarkastajan tulee antaa hallitukselle kirjallinen, seuran tilikokoukselle osoitettu kertomus toimittamastaan seuran hallinnon ja tilien tarkastuksesta viimeistään seitsemän (7) vuorokautta ennen tilikokousta.</p>
	<h2>15 § Sääntöjen muuttaminen</h2>
	<p>
	Muutoksia näihin sääntöihin voidaan tehdä, jos muutosesitys on tullut hyväksytyksi vähintään kolmen neljäsosan (3/4) enemmistöllä seuran kokouksessa. Seuran varsinaisella jäsenellä on oikeus tehdä hallitukselle esityksiä seuran sääntöjen muuttamiseksi. Sääntömuutosesitykset tulee jättää kirjallisina seuran hallituksen puheenjohtajalle. Hallituksen on käsiteltävä puheenjohtajalle jätetyt sääntömuutosesitykset kokouksessaan viipymättä.
	</p>

	<h2>16 § Seuran purkautuminen</h2>
	<p>
	Päätös seuran purkamisesta on tehtävä seuran kokouksessa vähintään kolmen neljäsosan (3/4) enemmistöllä annetuista äänistä. Seuran purkautuessa käytetään seuran varat seuran tarkoituksen edistämiseen purkamisesta päättävän kokouksen edellyttämällä tavalla. Seuran tullessa lakkautetuksi annetaan varat käytettäväksi Tampereen alueella toimivien raskasta musiikkia harrastavien tai tuottavien yhteisöjen toiminnan tukemiseen.
	</p>

